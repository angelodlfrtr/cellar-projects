class Milestone < ApplicationRecord

  # ==========================================================================================================
  # Relations ================================================================================================
  # ==========================================================================================================

  belongs_to :project
  has_many :tasks

  # ==========================================================================================================
  # Validations ==============================================================================================
  # ==========================================================================================================

  validates :name,
    presence: true,
    length: { minimum: 1, maximum: 64 }

  validates :deadline, presence: true
  validates :project, presence: true
end

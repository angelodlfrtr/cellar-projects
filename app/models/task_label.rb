class TaskLabel < ApplicationRecord

  # ==========================================================================================================
  # Relations ================================================================================================
  # ==========================================================================================================

  belongs_to :project

  # ==========================================================================================================
  # Validations ==============================================================================================
  # ==========================================================================================================

  validates :name,
    presence: true,
    length: { minium: 1, maximum: 16 }

  validates :color_code, presence: true

  # ==========================================================================================================
  # Callbacks ================================================================================================
  # ==========================================================================================================

  before_save :name_to_slug

  # ==========================================================================================================
  # Instance =================================================================================================
  # ==========================================================================================================

  private

    def name_to_slug
      self.name = self.name.parameterize
    end
end

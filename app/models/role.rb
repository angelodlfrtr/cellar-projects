class Role < ApplicationRecord

  # ==========================================================================================================
  # Relations ================================================================================================
  # ==========================================================================================================

  belongs_to :user
  belongs_to :project

  # ==========================================================================================================
  # Validations ==============================================================================================
  # ==========================================================================================================

  validates :user, :project, :level, presence: true
  validates :level, numericality: true, inclusion: { in: (0..3) }
end
